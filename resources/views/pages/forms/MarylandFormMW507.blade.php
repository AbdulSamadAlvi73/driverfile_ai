            </div>  
            <p class="mt-3"><b>Purpose.</b> Complete Form MW507 so that your employer can withhold the correct Maryland income tax from your pay. Consider completing a new Form MW507 each year and when your personal or financial situation changes.</p>
            <p><b>Basic Instructions.</b> Enter on line 1 below, the number of personal exemptions you will claim on your tax return. However, if you wish to claim more exemptions, or if your adjusted gross income will be more than $100,000 if you are filing single or married filing separately ($150,000, if you are filing jointly or as head of household), you must complete the Personal Exemption Worksheet on page 2. Complete the Personal Exemption Worksheet on page 2 to further adjust your Maryland withholding based on itemized deductions, and certain other expenses that exceed your standard deduction and are not being claimed at another job or by your spouse. However, you may claim fewer (or zero) exemptions.</p>
            <p><b>Additional withholding per pay period under agreement with employer.</b> If you are not having enough tax withheld, you may ask your employer to withhold more by entering an additional amount on line 2.</p>
            <p><b>Exemption from withholding.</b> You may be entitled to claim an exemption from the withholding of Maryland income tax if:  <br>
                a. Last year you did not owe any Maryland Income tax and had a right to a full refund of any tax withheld; AND, <br>
                b. This year you do not expect to owe any Maryland income tax and expect to have a right to a full refund of all income tax withheld. If you are eligible to claim this exemption, complete Line 3 and your employer will not withhold Maryland income tax from your wages. Students and Seasonal Employees whose annual income will be below the minimum filing requirements should claim exemption from withholding. This provides more income throughout the year and avoids the necessity of filing a Maryland income tax return.</p>
            <p><b>Certification of nonresidence in the State of Maryland.</b> Complete Line 4. This line is to be completed by residents of the District of Columbia, Virginia or West Virginia who are employed in Maryland and who do not maintain a place of abode in Maryland for 183 days or more. <br>
                Residents of Pennsylvania who are employed in Maryland and who do not maintain a place of abode in Maryland for 183 days or more, should complete line 5 to exempt themselves from the state portion of the withholding tax. These employees are still liable for withholding tax at the rate in effect for the Maryland county in which they are employed, unless they qualify for an exemption on either line 6 or line 7. Pennsylvania residents of York and Adams counties may claim an exemption from the local withholding tax by completing line 6. Pennsylvania residents living in other local jurisdictions which do not impose an earnings or income tax on Maryland residents may claim an exemption by completing line 7. Employees qualifying for exemption under 6 or 7, should also write "EXEMPT" on line 4. Line 4 is NOT to be used by residents of other states who are working in Maryland,because such persons are liable for Maryland income tax and withholding from their wages is required</p>
            <p>If you are domiciled in the District of Columbia, Pennsylvania or Virginia and maintain a place of abode in Maryland for 183 days or more, you become a statutory resident of Maryland and you are required to file a resident return with Maryland reporting your total income. You must apply to your domicile state for any tax credit to which you may be entitled under the reciprocal provisions of the law. If you are domiciled in West Virginia, you are not required to pay Maryland income tax on wage or salary income, regardless of the length of time you may have spent in Maryland. <br>
                Under the Servicemembers Civil Relief Act, as amended by the Military Spouses Residency Relief Act, you may be exempt from Maryland income tax on your wages if (i) your spouse is a member of the armed forces present in Maryland in compliance with military orders; (ii) you are present in Maryland solely to be with your spouse; and (iii) you maintain your domicile in another state. If you claim exemption under the SCRA enter your state of domicile (legal residence) on Line 8; enter "EXEMPT" in the box to the right on Line 8; and attach a copy of your spousal military identification card to Form MW507. <b>In addition, you must also complete and attach Form MW507M. </b></p>
            
            <p>Duties and responsibilities of employer. Retain this certificate with your records. You are required to submit a copy of this certificate and accompanying attachments to the Compliance Division, Compliance Programs Section, 301 West Preston Street, Baltimore, MD 21201-2326, when received if: <br>
                1. You have any reason to believe this certificate is incorrect; <br>
                2. The employee claims more than 10 exemptions;<br>
                3. The employee claims an exemption from withholding because he/she had no <br>
                tax liability for the preceding tax year, expects to incur no tax liability this year <br>
                and the wages are expected to exceed $200 a week; <br>
                4. The employee claims an exemption from withholding on the basis of nonresidence; or <br>
                5. The employee claims an exemption from withholding under the Military Spouses Residency Relief Act. <br>
                Upon receipt of any exemption certificate (Form MW507), the Compliance Division <br>
                will make a determination and notify you if a change is required. Once a certificate is revoked by the Comptroller, the employer must send any new certificate from the employee to the Comptroller for approval before implementing the new certificate. <br>
                If an employee claims exemption under 3 above, a new exemption certificate must be filed by February 15th of the following year. <br>
                Duties and responsibilities of employee. If, on any day during the calendar year, the number of withholding exemptions that the employee is entitled to claim is less than the number of exemptions claimed on the withholding exemption certificate in effect, the employee must file a new withholding exemption certificate with the employer within 10 days after the change occurs.</p>
