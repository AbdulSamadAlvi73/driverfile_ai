            <p>(a) An investigative consumer reporting agency shall supply files and information required under Section 1786.10 during normal business hours and on reasonable notice. <br> 
                (b) Files maintained on a consumer shall be made available for the consumer’s visual inspection, as follows: </p>
           <p>(1)	In person, if he appears in person and furnishes proper identification. A copy of his file shall also be available to the consumer for a fee not to exceed the actual costs of duplication services provided.  <br>
            (2)	By certified mail, if he makes a written request, with proper identification, for copies to be sent to a specified addressee.  Investigative consumer reporting agencies complying with requests for certified mailings under this section shall not be liable for disclosures to third parties caused by mishandling of mail after such mailings leave the investigative consumer reporting agencies. <br> 
            (3)	A summary of all information contained in files on a consumer and required to be provided by Section 1786.10 shall be provided by telephone, if the consumer has made a written request, with proper identification for telephone disclosure, and the toll charge, if any, for the telephone call is prepaid by or charged directly to the consumer. </p> 
                
            
            <p>(c)	The term “proper identification” as used in subdivision (b) shall mean that information generally deemed sufficient to identify a person.  Such information includes documents such as a valid driver’s license, social security account number, military identification card, and credit cards.  Only if the consumer is unable to reasonably identify himself with the information described above, may an investigative consumer reporting agency require additional information concerning the consumer’s employment and personal or family history in order to verify his identity. <br>
                (d)	The investigative consumer reporting agency shall provide trained personnel to explain to the consumer any information furnished him pursuant to Section 1786.10. <br>
                (e)	The investigative consumer reporting agency shall provide a written explanation of any coded information contained in files maintained on a consumer.  This written explanation shall be distributed whenever a file is provided to a consumer for visual inspection as required under Section 1786.22. <br>
                (f)	The consumer shall be permitted to be accompanied by one other person of his choosing, who shall furnish reasonable identification.  An investigative consumer reporting agency may require the consumer to furnish a written statement granting permission to the consumer reporting agency to discuss the consumer’s file in such person’s presence.</p>
        

