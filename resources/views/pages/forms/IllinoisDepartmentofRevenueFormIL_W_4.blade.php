                <h5>Form IL-W-4</h5>
                <hr>
                <h5 class="text-center">Employee’s and other Payee’s Illinois Withholding Allowance Certificate and Instructions</h5>
                <hr>
                <p>Note:  These instructions are written for employees to address withholding from wages.  However, this form can also be completed and submitted to a payor if an agreement was made to voluntarily withhold Illinois Income tax from other (non-wage) Illinois income.</p>
                <p><b>Who must complete Form IL-W-4? </b><br>
                    If you are an employee, you must complete this form so your employer can withhold the correct amount of Illinois Income Tax from your pay. The amount withheld from your pay depends, in part, on the number of allowances you claim on this form.</p>
                <p>Even if you claimed exemption from withholding on your federal Form W-4, U.S. Employee’s Withholding Allowance Certificate, because you do not expect to owe any federal income tax, you may be required to have Illinois Income Tax withheld from your pay (see Publication 130, Who is Required to Withhold Illinois Income Tax). If you are claiming exempt status from Illinois withholding, you must check the exempt status box on Form  IL-W-4 and sign and date the certificate. Do not complete Lines 1 through 3.</p>
                <p>If you are a resident of Iowa, Kentucky, Michigan, or Wisconsin, or a military spouse, see Form W-5-NR, Employees Statement of Nonresidence in Illinois, to determine if you are exempt.</p>
                <p><b>Note:</b> If you do not file a completed Form IL-W-4 with your employer, if you fail to sign the form or to include all necessary information, or if you alter the form, your employer must withhold Illinois Income Tax on the entire amount of your compensation, without allowing any exemptions.</p>
                <p><b>When must I submit this form? </b><br>
                    You should complete this form and give it to your employer on or before the date you start work. You must submit Form IL-W-4 when Illinois Income Tax is required to be withheld from compensation that you receive as an employee.  You may file a new Form IL-W-4 any time your withholding allowances increase. If the number of your claimed allowances decreases, you must file a new Form IL-W-4 within 10 days. However, the death of a spouse or a dependent does not affect your withholding allowances until the next tax year.</p>
                <p><b>When does my Form IL-W-4 take effect?</b> <br>
                    If you do not already have a Form IL-W4 on file with your employer, this form will be effective for the first payment of compensation made to you after this form is filed. If you already have a Form IL-W-4 on file with this employer, your employer may allow any change you file on this form to become effective immediately, but is not required by law to change your withholding until the first payment of compensation is made to you after the first day of the next calendar quarter (that is, January 1, April 1, July 1, or October 1) that falls at least 30 days after the date you file the change with your employer.</p>
                <p><b>Example:</b>  If you have a baby and file a new Form IL-W-4 with your employer to claim an additional allowance for the baby, your employer may immediately change the withholding for all future payments of compensation. However, if you file the new form on September 1, your employer does not have to change your withholding until the first payment of compensation is made to you after October 1. If you file the new form on September 2, your employer does not have to change your withholding until the first payment of compensation made to you after December 31.</p>
                <p><b>How long is Form IL-W-4 valid?</b> <br>
                    Your Form IL-W-4 remains valid until a new form you have submitted takes effect or until your employer is required by the Department to disregard it. Your employer is required to disregard your Form IL-W-4 if</p>
                <ol>
                    <li>you claim total exemption from Illinois Income Tax withholding, but you have not filed a federal Form W-4 claiming total exemption, or</li>
                    <li>the Internal Revenue Service (IRS) has instructed your employer to disregard your federal Form W-4</li>
                </ol>
                <p> <b>What is an "exemption"? </b><br>
                    An "exemption" is a dollar amount on which you do not have to pay Illinois Income Tax that you may claim on your Illinois Income tax return.</p>
                
                <p><b>What is an "allowance"?</b> <br>
                    The dollar amount that is exempt from Illinois Income Tax is based on the number of allowances you claim on this form. As an employee, you receive one allowance unless you are claimed as a dependent on another person’s tax return (e.g., your parents claim you as a dependent on their tax return). If you are married, you may claim additional allowances for your spouse and any dependents that you are entitled to claim for federal income tax purposes. You also will receive additional allowances if you or your spouse are age 65 or older, or if you or your spouse are legally blind</p>
                <p><b>Important:</b> For tax years beginning on or after January 1, 2017, the personal exemption allowance, and additional allowances if you or your spouse are age 65 or older, or if you or your spouse are legally blind, may not be claimed if the taxpayer’s adjusted gross income for the taxable year exceeds $500,000 for returns with a federal filing status of married filing jointly, or $250,000 for all other returns. Employees who exceed these income amounts may complete a new Form IL-W-4 to update their exemption amounts and increase their Illinois withholding.</p>
                <p><b>How do I figure the correct number of allowances?</b> <br> 
                    Complete the worksheet on the back of this page to figure the correct number of allowances you are entitled to claim. Give your completed Form IL-W-4 to your employer. Keep the worksheet for your records.</p>
                <p><b>Note:</b> If you have more than one job or your spouse works, your withholding usually will be more accurate if you claim all of your allowances on the Form IL-W-4 for the highest-paying job and claim zero on all of your other IL-W-4 forms.</p>
                <p><b>How do I avoid underpaying my tax and owing a penalty?</b> <br>
                    You can avoid underpayment by reducing the number of allowances or requesting that your employer withhold an additional amount from your pay. Even if your withholding covers the tax you owe on your wages, if you have non-wage income that is taxable, such as interest on a bank account or dividends on an investment, you may have additional tax liability. If you owe more than $500 tax at the end of the year, you may owe a late-payment penalty or will be required to make estimated tax payments. For additional information on penalties see Publication 103, Uniform Penalties and Interest. Visit our website at tax.illinois.gov to obtain a copy.</p>
                <b>Where do I get help?</b>
                <ul>
                    <li> Visit our website at tax.illinois.gov</li>
                    <li> Call our Taxpayer Assistance Division at <b>800 732-8866</b> or <b>217 782-3336</b></li>
                    <li>Call our TDD (telecommunications device for the deaf) at <b>800 544-5304</b></li>
                    <li> Write to <b>ILLINOIS DEPARTMENT OF REVENUE  PO BOX 19044  SPRINGFIELD IL 62794-9044</b></li>
                </ul>
