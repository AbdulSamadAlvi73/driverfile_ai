                    <p>Motor carriers have the responsibility to make the following investigations and inquiries with respect to each driver employed, other than a person who has been a regularly employed driver of the motor carrier for a continuous period which began before January 1, 1971.</p>

                    <ul class="mx-4">
                        <li>
                            (a)(1) An inquiry into the driver's driving record during the preceding three years to the appropriate agency of every State in which the driver held a motor vehicle operator's license or permit during those preceding three years.
                        </li> <br>
                        <li>(a)(2) An investigation of the driver's employment record during preceding three years.</li>
                        <br>
                        <li>(b) A copy of the driver record(s) obtained in response t the inquiry or inquiries to each State driver record agency as required must be placed in the Driver Qualification File within 30 days of the date the driver's employment begins and be retained in compliance with 391.51.</li>
                        <br>
                        <li>(c) Replies the the investigations of the driver's safety performance history must be placed in the Driver Investigation History File within 30 days of the date the driver's employment begins. This goes into effect after October 29, 2004.</li>
                        <br>
                        <li>(d) Prospective motor carrier must investigate the information from all previous employers of the applicant that employed the driver to operate a CMV within the previous three years. This information must cover general driver identification and employment verification information, data elements as specified in 390.15 for accident involving the driver that occurred in the three-year period preceding the date of the employment application, and accidents the previous employer may wish to provide.</li>
                        <br>
                        <li>(e) Prospective motor carriers must investigate the information from all previous DOT regulated employers that employed the driver within the previous three years from the date of the employment application in a safety-sensitive function that required alcohol and controlled substance testing specified by 49 CFR Part 40.</li>
                    </ul>

                    <hr>

                    <p class="h-1">Drivers have the following rights:</p>

                    <ol class="mx-4">
                        <li>The right to review information provided by previous employers</li>
                        <li>The right to have errors in the information corrected be the previous employer and for that employer to resend the corrected information to the prospective employer</li>
                        <li>The right to have a rebuttal statement attached to the alleged erroneous information, if the previous employer and the driver cannot agree on the accuracy of the information.</li>
                    </ol>
                    <p>Drivers who wish to review previous employer-provided information must submit a written request to the prospective employer when applying of as late as 30 days after employed or being notified of denial of employment. The prospective employer must provide this information to the applicant within five business days of receiving the written request. If the driver has not arranged to pick up or receive the requested records within 30 days pf the prospective employer making them available, the prospective motor carrier may consider the driver to have waived his/her request to review the records.</p>

                    <p>Drivers wishing to request correction of erroneous information in records must send the request for the correction to the previous employer that provided the records. After October 29, 2004, the previous employer must either correct and forward the information to the prospective motor carrier employer or notify the driver within 15 days of receiving the driver's request to correct the data that it does not agree to correct the data. Drivers wishing to rebut information in records must send the rebuttal to the previous employer with instructions to include the rebuttal in the driver's Safety Performance History.</p>

