            <p class="mt-3">Form W-4 (2021)</p>
            <p><b>Future developments.</b> For the latest information about any future developments related to Form W-4, such as legislation enacted after it was published, go to www.irs.gov/FormW4.</p>
            <p><b>Purpose.</b> Complete Form W-4 so that your employer can withhold the correct federal income tax from your pay. Consider completing a new Form W-4 each year and when your personal or financial situation changes.</p>
            <P>Exemption from withholding. You may claim exemption from withholding for 2019 if both of the following apply.</P>
            <ul>
                <li>For tax year 2020, you had a right to a refund of all South Carolina Income Tax withheld because you had no tax liability, and for tax year 2021 you expect a refund of all South Carolina Income Tax withheld because you expect to have no tax liability.</li>
                <li>Under the Servicemembers Civil Relief Act, you are claiming the same state of residence for tax purposes as your military servicemember spouse. You are only in South Carolina, or a bordering state, to be with your military spouse who is serving in the state in compliance with military orders. Provide your employer with a copy of your current military ID card and a copy of your spouse's latest Leave and Earnings Statement (LES). Your military ID card must have been issued within the last four years. The assignment location on the LES must be in South Carolina or a bordering state. Enter your spouse's state of domicile on the line provided. </li>
            </ul>
            <p>If you are exempt, complete only line 1 through line 4 and line 7. Check the box for the reason you are claiming an exemption and write Exempt on line 7. Your exemption for 2021 expires February 15, 2022. If you are a military spouse and you no longer qualify for the exemption, you have 10 days to update your SC W-4 with your employer. </p>
            <b>General Instructions</b>
            <p>If you aren't exempt, follow the rest of these instructions to determine the number of withholding allowances you should claim for withholding for 2021 and any additional amount of tax to have withheld. For regular wages, withholding must be based on allowances you claimed and may not be a flat amount or percentage of wages.</p>
            <p>You can also use the calculator at <b>www.irs.gov/W4App</b> to determine your tax withholding more accurately. Consider using this calculator if you have a more complicated tax situation, such as if you have a working spouse, more than one job, or a large amount of non-wage income not subject to withholding outside of your job. After your Form W-4 takes effect, you can also use this calculator to see how the amount of tax you're having withheld compares to your projected total tax for 2021. If you use the calculator, you don't need to complete any of the worksheets for Form W-4.</p>
            <p>Note that if you have too much tax withheld, you will receive a refund when you file your tax return. If you have too little tax withheld, you will owe tax when you file your tax return, and you might owe a penalty.</p>
            <p><b>Filers with multiple jobs or working spouses.</b> If you have more than one job at a time, or if you're married filing jointly and your spouse is also working, read all of the instructions including the instructions for the Two-Earners/Multiple Jobs Worksheet before beginning.</p>
            <p><b>Nonwage income.</b>If you have a large amount of nonwage income not subject to withholding, such as interest or dividends, consider making estimated tax payments using Form 1040-ES, Estimated Tax for Individuals. Otherwise, you might owe additional tax. Or, you can use the Deductions, Adjustments, and Additional Income Worksheet on page 3 or the calculator at www.irs.gov/W4App to make sure you have enough tax withheld from your paycheck. If you have pension or annuity income, see Pub. 505 or use the calculator at www.irs.gov/W4App to find out if you should adjust your withholding on Form W-4 or W-4P.</p>
            <p><b>Nonresident alien.</b> If you're a nonresident alien, see Notice 1392, Supplemental FormW-4 Instructions for Nonresident Aliens, before completing this form.</p>
            <h4>Specific Instructions</h4>
            <h4>Personal Allowances Worksheet</h4>
            <p>Complete this worksheet on page 3 first to determine the number of withholding allowances to claim.</p>
            <p><b>Line C. Head of household please note:</b>Generally, you may claim head of household filing status on your tax return only if you're unmarried and pay more than 50% of the costs of keeping up a home for yourself and a qualifying individual. See Pub. 501 for more information about filing status.</p>
            <p><b>Line E. Child tax credit. </b>When you file your tax return, you may be eligible to claim a child tax credit for each of your eligible children. To qualify, the child must be under age 17 as of December 31, must be your dependent who lives with you for more than half the year, and must have a valid social security number. To learn more about this credit, see Pub. 972, Child Tax Credit. To reduce the tax withheld from your pay by taking this credit into account, follow the instructions on line E of the worksheet. On the worksheet you will be asked about your total income. For this purpose, total income includes all of your wages and other income, including income earned by a spouse if you are filing a joint return.</p>
            <p><b>Line F.</b> Credit for other dependents. When you file your tax return, you may be eligible to claim a credit for other dependents for whom a child tax credit can't be claimed, such as a qualifying child who doesn't meet the age or social security number requirement for the child tax credit, or a qualifying relative. To learn more about this credit, see Pub. 972. To reduce the tax withheld from your pay by taking this credit into account, follow the instructions on line F of the worksheet. On the worksheet, you will be asked about your total income. For this purpose, total income includes all of your wages and other income, including income earned by a spouse if you are filing a joint return.</p>
