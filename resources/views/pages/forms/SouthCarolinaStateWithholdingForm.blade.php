                    <p><b>Line G.</b> Other credits. You may be able to reduce the tax withheld from your paycheck if you expect to claim other tax credits, such as tax credits for education (see Pub. 970). If you do so, your paycheck will be larger, but the amount of any refund that you receive when you file your tax return will be smaller. Follow the instructions for Worksheet 1-6 in Pub. 505 if you want to reduce your withholding to take these credits into account. Enter "-0-" on lines E and F if you use Worksheet 1-6.</p>
                    <b>Deductions, Adjustments, and Additional Income Worksheet</b>
                    <p>Complete this worksheet to determine if you'e able to reduce the tax withheld from your paycheck to account for your itemized deductions and other adjustments to income, such as IRA contributions. If you do so, your refund at the end of the year will be smaller, but your paycheck will be larger. You're not required to complete this worksheet or reduce your withholding if you don't wish to do so.</p>
                    <p>You can also use this worksheet to figure out how much to increase the tax withheld from your paycheck if you have a large amount of nonwage income not subject to withholding, such as interest or dividends.</p>
                    <p>Another option is to take these items into account and make your withholding more accurate by using the calculator at www.irs.gov/W4App. If you use the calculator, you don't need to complete any of the worksheets for Form W-4.</p>
                    <b>Two-Earners/Multiple Jobs Worksheet</b>
                    <p>Complete this worksheet if you have more than one job at a time or are married filing jointly and have a working spouse. If you don't complete this worksheet, you might have too little tax withheld. If so, you will owe tax when you file your tax return and might be subject to a penalty.</p>
                    <p>Figure the total number of allowances you're entitled to claim and any additional amount of tax to withhold on all jobs using worksheets from only one Form W-4. Claim all allowances on the W-4 that you or your spouse file for the highest paying job in your family and claim zero allowances on Forms W-4 filed for all other jobs. For example, if you earn $60,000 per year and your spouse earns $20,000, you should complete the worksheets to determine what to enter on lines 5 and 6 of your Form W-4, and your spouse should enter zero ("-0-") on lines 5 and 6 of his or her Form W-4. See Pub. 505 for details.</p>
                    <p>Another option is to use the calculator at www.irs.gov/W4App to make your withholding more accurate.</p>
                    <p>Tip: If you have a working spouse and your incomes are similar, you can check the "Married, but withhold at higher Single rate" box instead of using this worksheet. If you choose this option, then each spouse should fill out the Personal Allowances Worksheet and check the "Married, but withhold at higher Single rate" box on Form W-4, but only one spouse should claim any allowances for credits or fill out the Deductions, Adjustments, and Additional Income Worksheet.</p>
